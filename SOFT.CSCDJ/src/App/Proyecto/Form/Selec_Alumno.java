    public void ListarAlumno() {
        DefaultTableModel modelo = (DefaultTableModel) jTListaAlumno.getModel();
        while (modelo.getRowCount() > 0) {
            modelo.removeRow(0);
        }
        TableColumnModel modCol = jTListaAlumno.getColumnModel();
        Object[][] row = {};
        List<Persona> p = al.ListarAlumno();
        for (int i = 0; i < p.size(); i++) {
            String Estado_Persona = null;
            Persona per = new Persona();
            per = (Persona) p.get(i);
            if (per.getEstado_Alumno() == null) {
                Estado_Persona = "No Matriculado";
            } else if (per.getEstado_Alumno().equals("1")) {
                Estado_Persona = "No Matriculado";
            } else if (per.getEstado_Alumno().equals("2")) {
                Estado_Persona = "Matriculado";
            }
            modelo.addRow(new Object[]{per.getId_Persona(), per.getNombre_Persona(), per.getApellido_Paterno(), per.getApellido_Materno(), per.getDni(), Estado_Persona});
        }
        jTListaAlumno.setModel(modelo);
    }

