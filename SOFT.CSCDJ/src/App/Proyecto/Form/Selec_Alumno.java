    private void btnSeleccionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSeleccionarActionPerformed
        // TODO add your handling code here:
        int count = jTListaAlumno.getSelectedRowCount();
        if (count > 0) {
            int fila;
            fila = jTListaAlumno.getSelectedRow();
            int id_persona;
            String cod_estudiante;
            id_persona = (int) jTListaAlumno.getValueAt(fila, 0);
            List<Persona> list = al.ListarAlumnoxId(id_persona);
            if (list.size() > 0) {
                Persona per = new Persona();
                per = (Persona) list.get(0);
                if (per.getCod_Estudiante() != null) {
                    cod_estudiante = per.getCod_Estudiante();
                } else {
                    cod_estudiante = "Falta Ingresar";
                }
                FormMatricula.txtAlumno.setText(cod_estudiante + " : " + per.getNombre_Persona() + " " + per.getApellido_Paterno() + " " + per.getApellido_Materno());
                FormMatricula.id_persona = per.getId_Persona();
            }
            dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Seleccinar una fila de la tabla");
        }
    }//GEN-LAST:event_btnSeleccionarActionPerformed

