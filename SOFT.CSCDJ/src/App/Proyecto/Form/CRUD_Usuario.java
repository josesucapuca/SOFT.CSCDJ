    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        int nro = tblUsuarios.getSelectedColumnCount();
        if (nro > 0) {
            int fila = tblUsuarios.getSelectedRow();
            JFrame frame = new Usuario_Form();
            frame.setTitle("Modificar Usuario");
            frame.setLocationRelativeTo(null);
            frame.setDefaultCloseOperation(frame.EXIT_ON_CLOSE);
            frame.setVisible(true);
            Usuario_Form.lbltitle.setText("Modificar Usuario");
            Usuario_Form.btnRegistrarUsuario.setText("Modificar");
            Usuario_Form.id_Persona = Integer.parseInt((String) tblUsuarios.getValueAt(fila, 0));
            Usuario_Form.id_Usuario = Integer.parseInt((String) tblUsuarios.getValueAt(fila, 2));
            Usuario_Form.txtPersona.setText((String) tblUsuarios.getValueAt(fila, 1));
            Usuario_Form.txtUsuario.setText((String) tblUsuarios.getValueAt(fila, 3));
            Usuario_Form.txtPassword.setText((String) tblUsuarios.getValueAt(fila, 4));
            Usuario_Form.txtPasswordRe.setText((String) tblUsuarios.getValueAt(fila, 4));
            switch ((String) tblUsuarios.getValueAt(fila, 5)) {
                case "ACTIVADO":
                    Usuario_Form.cmbEstado_Usuario.setSelectedItem("ACTIVADO");
                    break;
                case "DESACTIVADO":
                    Usuario_Form.cmbEstado_Usuario.setSelectedItem("DESACTIVADO");
                    break;
                case "":
                    Usuario_Form.cmbEstado_Usuario.setSelectedItem("--");
                    break;

            }
            if (tblUsuarios.getValueAt(fila, 6).toString().equals("Sin Limite")) {
                Usuario_Form.txtFecha_Vencimiento.setDate(null);
            } else {
                Usuario_Form.txtFecha_Vencimiento.setDate(Fun.Cambiar_Fecha((String) tblUsuarios.getValueAt(fila, 6)));
            }
            Usuario_Form.MenSeleccionar.setEnabled(false);

        }

    }//GEN-LAST:event_btnModificarActionPerformed

